package com.jala.operator;

public class Comparisional
{
	public static void main(String[] args)
	{
		int x=10,y=47;
		System.out.println(x>y);
		System.out.println(x>=y);
		System.out.println(x<y);
		System.out.println(x<=y);
		System.out.println(x==y);
		System.out.println(x!=y);
	}
}

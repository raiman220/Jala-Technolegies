package com.jala.loop;

public class Larger
{
	public static void main(String[] args)
	{
		int a=10,b=23,c=34;
	    if (a >= b && a >= c)
	    {
	    	System.out.println(a+ " Is a largest Number!!!");
	    }
	    else if (b >= a && b >= c)
	    {
	    	System.out.println(b+ " Is a largest Number!!!");
	    }
	    else if (c >= a && c >= b)
	    {
	    	System.out.println(c+ " Is a largest Number!!!");
	    }
	  
	}
}
